namespace Web04Ex02;

class Program
{
    static void Main(string[] args)
    {
        int cont = 1;
        while (cont <= 10)
        {
            Console.Write(cont + " ");
            cont += 1;
        }
        
        int c = 10;
        while(c != 0)
        {
            Console.Write(c + " ");
            c -=1;
        }
    }
}
